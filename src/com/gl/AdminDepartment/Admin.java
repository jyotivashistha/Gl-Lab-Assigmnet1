package com.gl.AdminDepartment;

public class Admin {
	
	private String FirstName;
	private String LastName;
	
	public Admin(String Fname,String Lname) {
		FirstName=Fname;
		LastName=Lname;
	}

	public String getFirstName() {
		return FirstName;
	}

	public void setFirstName(String Jyoti) {
		FirstName = Jyoti;
	}

	public String getLastName() {
		return LastName;
	}

	public void setLastName(String Sharma) {
		LastName = Sharma;
	}

}
